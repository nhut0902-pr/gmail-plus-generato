function initSearchIndex() {
  if (!window.FlexSearch) {
    console.warn("FlexSearch kh√¥ng kh·∫£ d·ª•ng");
    return;
  }

  searchIndex = new FlexSearch({
    encode: "advanced",
    tokenize: "forward",
    async: true
  });

  // D·ªØ li·ªáu m·∫´u
  const items = [
    { id: 1, type: "H∆∞·ªõng d·∫´n", content: "Gmail cho ph√©p d√πng +t·ª´kh√≥a ƒë·ªÉ t·∫°o email ·∫£o", ref: "H∆∞·ªõng d·∫´n c√°ch d√πng" },
    { id: 2, type: "Ti·ªÅn t·ªë", content: "test shop news signup", ref: "test" },
    { id: 3, type: "Ti·ªÅn t·ªë", content: "dangky reg register", ref: "dangky" }
  ];

  items.forEach(item => {
    searchIndex.add(item.id, item.content);
    searchableContent.push(item);
  });
}

async function searchContent() {
  const input = document.getElementById("siteSearch");
  const resultsContainer = document.getElementById("searchResults");
  const query = input?.value.trim();

  if (!query || !resultsContainer || !searchIndex) {
    resultsContainer?.classList.remove("show");
    return;
  }

  try {
    const results = await searchIndex.search(query);
    if (results.length === 0) {
      resultsContainer.innerHTML = '<div class="no-result">Kh√¥ng t√¨m th·∫•y</div>';
    } else {
      let html = '';
      results.slice(0, 10).forEach(id => {
        const item = searchableContent.find(c => c.id == id);
        if (item) {
          html += `<div class="search-item" onclick="useSearchResult('${item.ref}')">
                     <strong>${item.type}</strong>: ${item.ref}
                   </div>`;
        }
      });
      resultsContainer.innerHTML = html;
    }
    resultsContainer.classList.add("show");
  } catch (err) {
    resultsContainer.classList.remove("show");
  }
}

function useSearchResult(text) {
  const prefixInput = document.getElementById("prefix");
  if (prefixInput) prefixInput.value = text.replace(/[^\w]/g, '').toLowerCase();
  document.getElementById("searchResults")?.classList.remove("show");
}

document.getElementById("siteSearch")?.addEventListener("input", searchContent);

